package com.mycompany.integradores_java;

/**
 *Realice un programa que muestre los numeros del 200 al 250 de a 2 en dos
 * @author andres-lopez
 */
public class De_dos_en_dos {
    public static void main (String [] args){
        // Vamos a utilizar un for para y el incremento lo colocamos de a 2
        for( int i = 200; i <= 250 ; i += 2 ){
            System.out.println(i);
        }
    }
}
